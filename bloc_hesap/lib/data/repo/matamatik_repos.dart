class MatamatikRepos {
  int toplamayap(String alinaSayi1, String alinansayi2) {
    int sayi1 = int.parse(alinaSayi1);
    int sayi2 = int.parse(alinansayi2);
    int toplam = sayi1 + sayi2;
    return toplam;
  }

  int carpmayap(String alinaSayi1, String alinansayi2) {
    int sayi1 = int.parse(alinaSayi1);
    int sayi2 = int.parse(alinansayi2);
    int carpma = sayi1 + sayi2;
    return carpma;
  }
}
